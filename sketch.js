function movement() {
	if (kb.pressing('a')) {
		player.rotate(-3, 3)
		player.direction = player.rotation;
	}
	if (kb.pressing('d')) {
		player.rotate(3, 3)
		player.direction = player.rotation;
	}
	if (kb.pressing('w')) {
		player.speed = 8;
		player.direction = player.rotation;
	}
	player.drag = 5;
	player.friction = 5;

}
function drawGame(){
	state = 'PLAY'

	startButton.hide()
	settingsButton.hide()
movement()
}

